let worldList = ["Japan", "USA", "Kenya", "Spain", "Nigeria"]

console.log(`Original array`, worldList)

// Sorting an Array
console.log(`After Sorting`, [...worldList].sort())
console.log(`Original Array`, worldList)

// reversing an Array
console.log(`reversing`, [...worldList].sort().reverse())

console.log(`Original Array`, worldList)

console.log(`Reversing Again`, [...worldList].sort())

console.log(`Original Array`, worldList)
